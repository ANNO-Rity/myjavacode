public class OperatorAritmatika {
    public static void main ( String [] args ) {
        //deklarasi dan INIT variable
       int  x = 10;
       int  y = 4;
        
       //menampilkan nilai variable
       System.out.println (" Nilai Variable : ");
       System.out.println (" x = " + x);
       System.out.println (" y = " + y);

       // menampilkan operator penjumlahan
       System.out.println (" Penjumlahan : ");
       System.out.println (" x + y =  " + (x + y));

       //menampilkan operator pengurangan
       System.out.println (" Pengurangan ");
       System.out.println (" x - y = " + ( x  - y));

       //menampilkan operator perkalian
       System.out.println (" Perkalian ");
       System.out.println (" x * y = " + ( x * y));

       //menampilkan operator pembagian
       System.out.println (" Pembagian ");
       System.out.println (" x / y = " + ( x / y));

       //menampilkan operator modulus 
       System.out.println (" Modulus ");
       System.out.println (" x % y = " + ( x % y));
    }
}
