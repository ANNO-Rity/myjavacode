public class OP_Penambah_dan_Pengurang {
    public static void main(String[] args) {
        
        int a, b;
        a = 20;
        b = 5;

        System.out.println("Nilai A = " + a);
        System.out.println("Nilai ++A = " + ++a);
        System.out.println("Nilai A++ = " + a++);
        System.out.println("Nilai A= " + a);
        System.out.println("-------------------------------");
        System.out.println("Nilai B = " + b);
        System.out.println("Nilai ++B = " + ++b);
        System.out.println("Nilai B++ = " + b++);
        System.out.println("Nilai B= " + b);
    }
}
